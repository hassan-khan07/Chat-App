// higher order function : wo function jo function ko as a parameter accept kar sakta hain ya phr usa return kar sakta hain. as a varaible hota hain
const asyncHandler = (requestHandler) => {
  return (req, res, next) => {
    Promise.resolve(requestHandler(req, res, next)).catch((err) => next(err));
  };
};

export { asyncHandler };

